// parseInt
// INT 타입으로 변경 10진수 정수형
console.log('parseInt: ')
console.log(parseInt('1'))
console.log(parseInt('1.1'))
console.log(parseInt(1))
console.log(parseInt(1.1))

// parseFloat
// 실수 타입으로 변경
console.log('parseFloat: ')
console.log(parseFloat('1'))
console.log(parseFloat('1.1'))
console.log(parseFloat(1))
console.log(parseFloat(1.1))
